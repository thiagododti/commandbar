CREATE SCHEMA commandbardb;
USE commandbardb;

create table funcionarios(
FUNC_ID INT PRIMARY KEY AUTO_INCREMENT,
FUNC_NAME VARCHAR(30) NOT NULL,
FUNC_CPF VARCHAR(20) NOT NULL,
FUNC_PASS VARCHAR(100) NOT NULL,
FUNC_CARG VARCHAR(100)NOT NULL,
FUNC_SAL DOUBLE NOT NULL,
FUNC_QTATEND BIGINT,
FUNC_ADMDATE DATE NOT NULL,
FUNC_DMSDATE DATE,
FUNC_TOKEN VARCHAR(100)
);